    <h2>About RefBooru</h2>

    <p>
        RefBooru is a tag based reference lookup website. <br> <br>

        As an artist I've always been looking for the best website to find references for my drawings, 
        and I could never find one that would give me exactly what I was searching for. <br> <br>

        Each image has specific tags associated with it that describe what the image is. 
        For example, if the user searches for the tag "Hands", every image that has that tag will be displayed back to them. <br> <br>

        Right now the number of images that we have on file is quite low.. only around 50 images, 
        but over time I plan to add a plethora of images to fill every reference type possible.
    </p>
